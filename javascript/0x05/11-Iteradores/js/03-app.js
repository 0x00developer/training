/**
 * Print "fizz" for multiples of 3
 * Print "buzz" for multiples of 5
 * Print "fizzbuzz" for multiples of both
 */
for (let i = 1; i <= 100; i++) {
  if (i % 3 === 0 && i % 5 === 0) {
    console.log("fizzbuzz");
  } else if (i % 3 === 0) {
    console.log("fizz");
  } else if (i % 5 === 0) {
    console.log("buzz");
  } else {
    console.log(i);
  }
}
